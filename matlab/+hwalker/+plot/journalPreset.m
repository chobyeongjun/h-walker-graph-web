        validNames = strjoin(fieldnames(presets), ', ');
        error('hwalker:plot:unknownJournal', ...
            'Unknown journal ''%s''. Valid: %s', name, validNames);
    end

    p = presets.(name);
    p = postProcess(p, wongColors, grayscaleColors, defaultColors, elsevierColors);
