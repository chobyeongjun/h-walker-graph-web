            error('hwalker:plot:customNeedsStruct', ...
                'Custom preset requires a struct: hwalker.plot.journalPreset(''Custom'', struct(...))');
        end
        p = mergeWithDefaults(varargin{1});
        p = postProcess(p, wongColors, grayscaleColors, defaultColors, elsevierColors);
