function p = postProcess(p, wongColors, grayscaleColors, defaultColors, elsevierColors)
    switch p.paletteName
        case 'wong',      p.palette = wongColors;
        case 'grayscale', p.palette = grayscaleColors;
        case 'elsevier',  p.palette = elsevierColors;
        otherwise,        p.palette = defaultColors;
    end
    % Backward compatibility: legacy code expected `colors`
    p.colors = p.palette;

    % Inches conversions  (1 inch = 25.4 mm)
    p.col1in   = p.col1mm   / 25.4;
    p.col2in   = p.col2mm   / 25.4;
    p.col1h_in = p.col1h_mm / 25.4;
    p.col2h_in = p.col2h_mm / 25.4;
    if isfield(p, 'col15mm') && isfinite(p.col15mm)
        p.col15in   = p.col15mm   / 25.4;
        p.col15h_in = p.col15h_mm / 25.4;
    else
        p.col15mm   = NaN;  p.col15h_mm = NaN;
        p.col15in   = NaN;  p.col15h_in = NaN;
    end
end


function s = mergeWithDefaults(userStruct)
% Fill in missing fields with sensible defaults so user can pass a partial struct.
    defaults.name           = 'Custom';
    defaults.full           = 'Custom user-defined preset';
    defaults.col1mm         = 88.9;
    defaults.col1h_mm       = 65.0;
    defaults.col2mm         = 181.0;
    defaults.col2h_mm       = 90.0;
    defaults.col15mm        = NaN;
    defaults.col15h_mm      = NaN;
    defaults.maxHmm         = 230.0;
    defaults.font           = 'Arial';
    defaults.fontFallback   = {'Arial','Helvetica','sans-serif'};
    defaults.bodyPt         = 8;
    defaults.axisPt         = 8;
    defaults.legendPt       = 7;
    defaults.titlePt        = 10;
    defaults.strokePt       = 0.75;
    defaults.axesPt         = 0.6;
    defaults.gridPt         = 0.3;
    defaults.paletteName    = 'wong';
    defaults.colorblindSafe = true;
    defaults.bg             = '#ffffff';
    defaults.axisColor      = '#000000';
    defaults.gridColor      = '#DDDDDD';
    defaults.formats        = {'PDF','EPS','PNG','TIFF'};
    defaults.dpi            = 300;
    defaults.interpreter    = 'tex';
    defaults.notes          = 'User-defined custom preset';

    s = defaults;
    fns = fieldnames(userStruct);
    for i = 1:numel(fns)
        s.(fns{i}) = userStruct.(fns{i});
    end
end


