    s.col15mm        = col15mm;
    s.col15h_mm      = col15h_mm;
    s.maxHmm         = maxHmm;
    s.font           = font;
    s.fontFallback   = fontFallback;
    s.bodyPt         = bodyPt;
    s.axisPt         = axisPt;
    s.legendPt       = legendPt;
    s.titlePt        = titlePt;
    s.strokePt       = strokePt;
    s.axesPt         = axesPt;
    s.gridPt         = gridPt;
    s.paletteName    = paletteName;
    s.colorblindSafe = colorblindSafe;
    s.bg             = bg;
    s.axisColor      = axisColor;
    s.gridColor      = gridColor;
    s.formats        = formats;
    s.dpi            = dpi;
    s.interpreter    = interpreter;
    s.notes          = notes;
end
